// console.log() - Console is an object and log is a method

// Simplest form of object attribution

const luiz = {      // Object is a group of values - key-value pair
    name: 'Luiz Sachser',
    age: 39
};

console.log(luiz.name);
console.log(luiz.age);
console.log(luiz);

// Add another attribute.
luiz.height = 1.91; 

//It can also delete attibutes using 'delete luiz.name' for example

console.log(luiz);
